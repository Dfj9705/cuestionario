                    Una contraseña robusta es la que está diseñada para que sea difícil de descubrir para una persona o un programa. Ya que el propósito de una contraseña es asegurar que solo los usuarios autorizados pueden acceder a los recursos, una contraseña que es fácil
                    de adivinar es un riesgo de seguridad. Siempre debemos elegir una contraseña con un mínimo de ocho caracteres y que a ser posible combine mayúsculas, minúsculas, números y símbolos. Un truco para memorizar contraseñas
                    complejas consiste en recurrir a poemas, letras de canciones o alguna otra frase que nos resulte familiar y que incluya algún número. A partir de ahí, podemos construir una contraseña robusta si empleamos solo
                    las iniciales de cada palabra y los dígitos, comenzando en mayúscula y terminando con algún símbolo, como %. Por ejemplo, si tomamos como referencia la oración “El 7 de septiembre es nuestro aniversario”, nuestra
                    clave sería E7dsena%.
