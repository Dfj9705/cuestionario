                    engaño a una víctima ganándose su confianza haciéndose pasar por una persona, empresa o 
                    servicio de confianza, para manipularla y hacer que realice acciones que
                    no debería realizar. Phishing es una modalidad de robo de identidad en la cual los 
                    delincuentes se valen del correo electrónico para conseguir con engaños que sus víctimas 
                    ingresen en sitios web falsos.                       
                </p>
