                6. NO INGRESAR A LA DARK WEEB O DEEP WEEB
            </button>
        </h2>
        <div id="subtema6" class="accordion-collapse collapse" style="text-align: justify;" aria-labelledby="title6" data-bs-parent="#accordionModulo1">
            <div class="accordion-body bg-dark text-white">
                <p class="fs-5"> 
                    
                    Este concepto se refiere a una parte de la red que está oculta y a 
                    la que solo se puede ingresar por medio de un protocolo de comunicación 
                    especial. Para comprender su definición se debe entender que 
                    internet está dividido en tres niveles: la Clear web, la Deep web y la 
                    Dark Web.
                                                        
                </p>
                <div class="row justify-content-center mb-3">
                    <div class="col-lg-6">
                        <img width="100%" src="../assets/img/darkweb.png" alt="imagen-darkweeb">
                        
                    </div>
                </div>
                <h3>Clear web</h3>
                <p class="fs-5">         
                    La Clear Web, hace referencia a ese nivel de la red al que todos pueden acceder fácilmente desde cualquier navegador, en otras palabras, el internet que hemos conocido y usado siempre y en el que se pueden realizar búsquedas con motores como Google o Bing.
                </p>
                
                <h3>Deep web</h3>
                <p class="fs-5">         
                    La Deep Web, hace referencia a los sitios que no están indexados y que no se encuentran por medio de búsquedas. Por ejemplo, la página de un banco en la que se realiza una transacción o un archivo adjunto en un correo electrónico o un servidor.
                </p>
                
                <h3>Dark web</h3>
                <p class="fs-5">         
                    Dark Web,hace referencia a que requiere de más procedimientos, usa direcciones IP enmascaradas accedidas por medio de navegadores específicos a través de protocolos especiales.
                </p>

                <div class="row justify-content-center mb-3">
                    <div class="col-lg-6">
                        <img width="100%" src="../assets/img/deepweb.webp" alt="imagen-deepweb">
                        
                    </div>
                </div>

                <p class="fs-5">         
                    Ingresar a la "Dark Web" o "Red Oscura" significa entrar a un mundo tenebroso en donde se halla, en gran parte, productos, servicios y toda clase de publicaciones que, para muchos, representan el peor lado de la humanidad.
                </p>

                <h3>Razones para nunca entrar a la DeepWeeb</h3>
                <ol class="fs-5"> 
                    <li>
                        <span class="fw-bold"> La dirección IP</span> es una «dirección» que puede brindar tu ubicación Geográfica, independientemente de usar o no Proxys, Switches, Redirects o lo que sea.                    
                    </li>
                    <li>
                        <span class="fw-bold">La información es Bidireccional</span> Así como nuestro computador recibe información de la misma manera debe enviarla al servidor o página web a la que nos conectamos, brindando información acerca de nuestro sistema operativo, navegador, MAC, IP y hasta información personal.                    
                    </li>
                    <li>
                        <span class="fw-bold">Navegar por la Deep Web no es ilegal </span>, pero ciertas acciones en la Deep Web sí pueden llevarte a tener problemas legales. Si esta información disponible en la Deep Web esta oculta al público es porque definitivamente es ilegal, además la CIA, el FBI y otras instituciones de justicia buscan a criminales.
                    </li>
                    <li>  
                        <span class="fw-bold">Se arriesga mucho, se gana poco</span> Así de simple, no hay contenido que tenga tanto valor como para obligarnos a buscar en la Deep Web, lo que ya hemos encontrado en el Internet Normal o ClearNet como le llaman en la Deep. Entonces arriesgarse a ser seguido por un hacker o entidad de justicia, no vale a la pena, además del contenido imprudente que podamos encontrar allí.
                    </li>
                    <li>
                        <span class="fw-bold">Es un lugar desordenado </span>,  Si teniendo a Google dentro de Internet que indexa gran cantidad de contenido diariamente, es aún difícil encontrar información de interés dentro de Internet, ahora en un lugar
                        como la Deep Web donde hay un sin número de directorios con enlaces caídos o bien redireccionando a sitios web donde probablemente te infectes de algún virus.
                                
                    </li>
                </ol>
              
