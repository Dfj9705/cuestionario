                3. NO INGRESAR A PÁGINAS QUE TENGAN EL PROTOCCÓLO DE SEGURIDAD (http://).
            </button>
        </h2>
        <div id="subtema3" class="accordion-collapse collapse" style="text-align: justify;" aria-labelledby="title3" data-bs-parent="#accordionModulo1">
            <div class="accordion-body bg-dark text-white">
                <p class="fs-5"> 
                    

                    El protocolo seguro de transferencia de hipertexto (HTTPS) es una 
                    versión mejorada del HTTP convencional a la que se le han añadido 
                    los protocolos de seguridad SSL/TLS (Secure Sockets Layer/Transmission 
                    Layer Security), que sirven para cifrar los paquetes
                    que se envían a través de la World Wide Web. Cuando 
                    accedes a una web y antes de la dirección aparece 
                    http:// es porque estás utilizando el protocolo HTTP, y 
                    cualquier dato que envíes a la web se transmite en texto
                    plano, sin cifrar. Esto quiere decir que cualquiera que 
                    se conecte a tu red WiFi, o que tenga acceso a la comunicación 
                    entre tu ordenador y el servidor (tu ISP, por ejemplo) puede 
                    ver todos los datos que recibes
                    y envías, incluidas tus contraseñas.
                                                                                   
                </p>
                <div class="row justify-content-center">
                    <div class="col-lg-8">
                        <img width="100%" src="../assets/img/httpshttp.jpg" alt="imagen-diferencia-https">
                        
                    </div>
                </div>
                <p class="fs-5"> 

                    Mientras, cuando entras a una web y antes de la dirección 
                    aparece https:// es porque has entrado a una página que 
                    utiliza el protocolo HTTPS, y que por lo tanto cifra todos 
                    los datos que le envías. Esto quiere decir que los datos 
                    viajan codificados hasta el servidor de la web, y que es 
                    allí donde se descifran para que no se puedan ver durante 
                    el camino.
                  
                </p>
                <p class="fs-5"> 

        
                    El cifrado de este protocolo es bidireccional, por
                    lo que funciona tanto cuando tú te conectas a una 
                    web como cuando un servicio online accede a tu ordenador. Eso hace
                    que sea muchísimo más difícil poder interferir entrando en medio de 
                    la comunicación, lo que te ayuda a prevenir ser espiados o 
                    que manipulen tus comunicaciones. Además, con el HTTPS también sabrás que una página
                    es realmente la original y no una imitación que quiera llevarse tus datos.
                                        
 
                </p>
                <div class="row justify-content-center">
